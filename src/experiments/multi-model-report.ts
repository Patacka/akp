#!/usr/bin/env tsx
/**
 * multi-model-report.ts — Aggregate all sweep-*.json files into a comparison table.
 *
 * Usage:
 *   npx tsx src/experiments/multi-model-report.ts
 *   npx tsx src/experiments/multi-model-report.ts --latex   # also emit LaTeX table
 *
 * Reads results/sweep-*.json produced by multi-model-sweep.ts.
 */

import { readdirSync, readFileSync } from 'node:fs'
import { join } from 'node:path'
import type { ExperimentSummary } from './metrics.js'

interface SweepFile {
  label: string
  janModelId: string
  timestamp: string
  results: Record<string, ExperimentSummary>
}

const EXPERIMENTS = ['E1', 'E2', 'E7']
const EXP_NAMES: Record<string, string> = {
  E1: 'Consensus Formation',
  E2: 'Adversarial Detection',
  E7: 'Contradiction Injection',
}

function load(): SweepFile[] {
  let files: string[]
  try {
    files = readdirSync('results').filter(f => f.startsWith('sweep-') && f.endsWith('.json'))
  } catch {
    console.error('No results/ directory found. Run multi-model-sweep.ts first.')
    process.exit(1)
  }
  if (files.length === 0) {
    console.error('No sweep-*.json files found in results/. Run multi-model-sweep.ts first.')
    process.exit(1)
  }
  return files.map(f => JSON.parse(readFileSync(join('results', f), 'utf8')) as SweepFile)
}

const pct = (v: number) => isNaN(v) ? ' n/a ' : `${(v * 100).toFixed(1)}%`
const f2  = (v: number) => isNaN(v) ? ' n/a' : v.toFixed(2)

function printConsoleTable(sweeps: SweepFile[]) {
  const labelW = Math.max(12, ...sweeps.map(s => s.label.length)) + 2
  const colW = 10

  // Header
  const header = 'Model'.padEnd(labelW) +
    EXPERIMENTS.map(e => e.padEnd(colW)).join('') +
    'Avg Acc'.padEnd(colW) +
    'Avg Conf'.padEnd(colW) +
    'Avg Gini'
  console.log('\n' + '═'.repeat(header.length))
  console.log('  AKP MULTI-MODEL COMPARISON  (E1=Consensus, E2=Adversarial, E7=Contradiction)')
  console.log('  Accuracy / mean confidence / Gini per model across three LLM-dependent experiments')
  console.log('═'.repeat(header.length))
  console.log(header)
  console.log('─'.repeat(header.length))

  for (const sweep of sweeps) {
    const accs: number[] = []
    const confs: number[] = []
    const ginis: number[] = []

    let row = sweep.label.padEnd(labelW)
    for (const exp of EXPERIMENTS) {
      const s = sweep.results[exp]
      if (!s) { row += 'missing'.padEnd(colW); continue }
      const acc = s.verdictAccuracyRate
      accs.push(isNaN(acc) ? 0 : acc)
      confs.push(isNaN(s.meanFinalConfidence) ? 0 : s.meanFinalConfidence)
      ginis.push(isNaN(s.reputationGini) ? 0 : s.reputationGini)
      row += pct(acc).padEnd(colW)
    }
    const avgAcc  = accs.reduce((s, v) => s + v, 0) / accs.length
    const avgConf = confs.reduce((s, v) => s + v, 0) / confs.length
    const avgGini = ginis.reduce((s, v) => s + v, 0) / ginis.length
    row += pct(avgAcc).padEnd(colW) + f2(avgConf).padEnd(colW) + f2(avgGini)
    console.log(row)
  }
  console.log('═'.repeat(header.length))
  console.log()
}

function printLatexTable(sweeps: SweepFile[]) {
  const lines: string[] = []
  lines.push('% ── Multi-model comparison table ────────────────────────────')
  lines.push('% Generated by: npx tsx src/experiments/multi-model-report.ts --latex')
  lines.push('\\begin{table}[t]')
  lines.push('\\centering')
  lines.push('\\small')
  lines.push('\\setlength{\\tabcolsep}{4pt}')
  lines.push('\\caption{Multi-model cross-architecture results. Accuracy = \\% verdicts')
  lines.push('  matching ground truth; $\\bar{C}$ = mean final KU confidence;')
  lines.push('  Gini = reputation concentration. Experiments: E1=Consensus Formation,')
  lines.push('  E2=Adversarial Detection, E7=Contradiction Injection.}')
  lines.push('\\label{tab:multimodel}')
  lines.push('\\begin{tabular}{lcccccccc}')
  lines.push('\\toprule')
  lines.push('\\textbf{Model} &')
  lines.push(EXPERIMENTS.map(e => `\\multicolumn{2}{c}{\\textbf{${EXP_NAMES[e]?.split(' ')[0] ?? e}}}`).join(' & ') + ' \\\\')
  lines.push(' & ' + EXPERIMENTS.map(() => 'Acc & $\\bar{C}$').join(' & ') + ' \\\\')
  lines.push('\\midrule')

  for (const sweep of sweeps) {
    const cells: string[] = [sweep.label.replace(/_/g, '\\_')]
    for (const exp of EXPERIMENTS) {
      const s = sweep.results[exp]
      if (!s) { cells.push('--', '--'); continue }
      cells.push(`${(s.verdictAccuracyRate * 100).toFixed(1)}\\%`, s.meanFinalConfidence.toFixed(2))
    }
    lines.push(cells.join(' & ') + ' \\\\')
  }

  lines.push('\\bottomrule')
  lines.push('\\end{tabular}')
  lines.push('\\end{table}')

  console.log('\n% ── LaTeX table (paste into paper) ─────────────────────────')
  console.log(lines.join('\n'))
  console.log()
}

function printDetailedBreakdown(sweeps: SweepFile[]) {
  for (const sweep of sweeps) {
    console.log(`\n── ${sweep.label}  (${sweep.janModelId})  ${sweep.timestamp.slice(0,10)} ──`)
    for (const exp of EXPERIMENTS) {
      const s = sweep.results[exp]
      if (!s) { console.log(`  ${exp}: missing`); continue }
      console.log(
        `  ${exp}: accuracy=${pct(s.verdictAccuracyRate).trim()}` +
        `  reveal_ok=${pct(s.revealSuccessRate).trim()}` +
        `  conf=${f2(s.meanFinalConfidence)}` +
        `  gini=${f2(s.reputationGini)}` +
        `  blacklist=${pct(s.blacklistRate).trim()}` +
        `  commits=${s.totalCommits}`
      )
    }
  }
  console.log()
}

function main() {
  const sweeps = load()
  const latex = process.argv.includes('--latex')
  const verbose = process.argv.includes('--verbose')

  // Sort alphabetically by label for stable output
  sweeps.sort((a, b) => a.label.localeCompare(b.label))

  printConsoleTable(sweeps)
  if (verbose) printDetailedBreakdown(sweeps)
  if (latex) printLatexTable(sweeps)

  // Suggest paper update
  console.log(`Found ${sweeps.length} model(s): ${sweeps.map(s => s.label).join(', ')}`)
  console.log('Run with --latex to generate the LaTeX table for the paper.')
  console.log('Run with --verbose for per-experiment breakdowns.')
}

main()
